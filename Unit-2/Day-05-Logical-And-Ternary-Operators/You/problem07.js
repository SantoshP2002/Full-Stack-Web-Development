// switch case;

let weather_app = "candy";


switch(weather_app){
    case "sunny":{
        console.log("its a sunny day");
        break;
    }

    case "cloudy":{
        console.log("its a cloudy day");
        break;
    }
    case "rainy":{
        console.log("its a rainy day");
        break;
    }
    default: {
        console.log("please check your internet connection");
    }
}